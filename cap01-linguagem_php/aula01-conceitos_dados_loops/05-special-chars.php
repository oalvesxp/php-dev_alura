 echo "Hello world!\n"; // \n: Nova linha
 echo "Hello world!\r"; // \r: Retorno de carro (carriage return)
 echo "Hello world!\t"; // \t: Tabulação (tab)
 echo "Caminho do arquivo: C:\\diretorio\\arquivo.txt"; // \\: Barra invertida
 echo 'I\'m learning PHP.'; // \': Aspa simples.
 echo "She said, \"Hello world!\""; // \": Aspa dupla.
 
 $preco = 20;
 echo "O preço é \$" . $preco; // \$: Símbolo de dólar.

 echo "Hello\0world"; // \0: Caractere nulo (null byte).
 echo "\n";